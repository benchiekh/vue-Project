<template>
  <v-app>
    <v-main>
      <v-container class="fill-height" fluid>
        <v-row align="center" justify="center">
          <v-col cols="12" sm="8" md="6">
            <v-card class="elevation-12 pa-4">
              <v-card-title class="text-center">
                <h1 class="display-2">Sign In</h1>
              </v-card-title>
              
              <v-card-subtitle class="text-center mb-4">
                <div class="social-buttons">
                  <v-btn fab color="blue darken-2" outlined>
                    <v-icon>fab fa-facebook-f</v-icon>
                  </v-btn>
                  <v-btn fab color="red darken-2" outlined>
                    <v-icon>fab fa-google</v-icon>
                  </v-btn>
                  <v-btn fab color="blue darken-3" outlined>
                    <v-icon>fab fa-linkedin-in</v-icon>
                  </v-btn>
                  <v-btn fab color="blue lighten-1" outlined>
                    <v-icon>fab fa-twitter</v-icon>
                  </v-btn>
                  <v-btn fab color="black" outlined>
                    <v-icon>fab fa-github</v-icon>
                  </v-btn>
                  <v-btn fab color="orange darken-4" outlined>
                    <v-icon>fab fa-gitlab</v-icon>
                  </v-btn>
                  <v-btn fab color="blue darken-3" outlined>
                    <v-icon>fab fa-bitbucket</v-icon>
                  </v-btn>
                </div>
              </v-card-subtitle>
              
              <v-card-text>
                <v-form @submit.prevent="login">
                  <v-text-field
                    v-model="email"
                    label="Email"
                    prepend-icon="mdi-email"
                    type="text"
                    :rules="[rules.required, rules.email]"
                    color="blue accent-3"
                    required
                  ></v-text-field>
                  <v-text-field
                    v-model="password"
                    label="Password"
                    prepend-icon="mdi-lock"
                    type="password"
                    :rules="[rules.required, rules.minLength]"
                    color="red accent-3"
                    required
                  ></v-text-field>
                  
                  <div class="text-center mt-4">
                    <v-btn @click="login" rounded color="primary" class="mx-2">
                      Sign In
                    </v-btn>
                  </div>
                </v-form>
              </v-card-text>
              
             
              
              <v-card-actions class="text-center">
                <v-btn
                  rounded
                  outlined
                  dark
                  @click="goToRegister"
                >
                  Sign Up
                </v-btn>
              </v-card-actions>
            </v-card>
          </v-col>
        </v-row>
      </v-container>
    </v-main>
  </v-app>
</template>

<script>
import axios from 'axios';
import { defineComponent } from 'vue';

export default defineComponent({
  name: 'UserLogin',
  data() {
    return {
      email: '',
      password: '',
      rules: {
        required: value => !!value || 'Required.',
        email: value => /.+@.+/.test(value) || 'E-mail must be valid.',
        minLength: value => value.length >= 6 || 'Password must be at least 6 characters long.',
      },
    };
  },
methods: {
  async login() {
    try {
      // Envoyer la requête POST avec l'email et le mot de passe
      const response = await axios.post('http://localhost:5000/api/auth/login', {
        email: this.email, // Utiliser "email" ici pour correspondre au backend
        password: this.password,
      });

      // Afficher la réponse pour déboguer
      console.log('Login response:', response.data);

      // Vérifier si la requête a été un succès
      if (response.status === 200) {
        const userRole = response.data.role; // Extraire le rôle de l'utilisateur depuis la réponse
        console.log('User role from response:', userRole);

        // Redirection en fonction du rôle de l'utilisateur
        if (userRole === 'admin') {
          this.$router.push({ name: 'Admindashboard' });
        } else if (userRole === 'user') {
          this.$router.push({ name: 'Userdashboard' });
        } else {
          alert('Invalid user role');
        }
      } else {
        alert('Login failed. Please try again.');
      }
    } catch (error) {
      // Afficher l'erreur en cas d'échec
      console.error('There was an error!', error.response?.data || error);
      alert('Login failed. Please try again.');
    }
  },
  
  // Méthode pour rediriger vers la page d'enregistrement
  goToRegister() {
    this.$router.push({ name: 'Register' });
  },
},

});
</script>

